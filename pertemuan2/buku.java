package pertemuan2;

public class buku {
    
    private String judul;
    private String penulis;

    public buku(String judul, String penulis) {
        this.judul = judul;
        this.penulis = penulis;
    }

    public String getJudul() {
        return judul;
    }

    public String getPenulis() {
        return penulis;
    }
}
